export const getProductsFiltered = (
  category = null,
  subcategory = null,
  collection = null
) => {
  let filtered = products;

  if (category) {
    filtered = filtered.filter(
      (p) => p.category.toLowerCase() === category.toLowerCase()
    );
  }

  if (subcategory) {
    filtered = filtered.filter(
      (p) => p.subcategory.toLowerCase() === subcategory.toLowerCase()
    );
  }

  if (collection) {
    filtered = filtered.filter(
      (p) =>
        p.collection && p.collection.toLowerCase() === collection.toLowerCase()
    );
  }

  return filtered;
};

export const getAllCollectionNames = () => {
  const collectionNames = new Set();
  products.forEach((product) => {
    if (product.collection) {
      collectionNames.add(product.collection);
    }
  });
  return Array.from(collectionNames);
};

export const getProductsByCollection = () => {
  const collections = {};
  products.forEach((product) => {
    const collectionName = product.collection;
    if (collectionName) {
      if (!collections[collectionName]) {
        collections[collectionName] = [];
      }
      collections[collectionName].push(product);
    }
  });
  return collections;
};
